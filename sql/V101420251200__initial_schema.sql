ON COLUMN USERS.ROLE_TYPE IS 'USER ROLE IN THE SYSTEM';
COMMENT
ON COLUMN USERS.ROLE_TYPE IS 'USER ROLE IN THE SYSTEM';
COMMENT
ON TABLE BOOK_ITEM_HISTORY IS 'CREATE THE BOOK_ITEM_HISTORY TABLE TO STORE INFORMATION ABOUT EACH BOOK ACTION.';
